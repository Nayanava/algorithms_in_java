package com.codeforces.div.second.r628;

import java.util.Scanner;

public class Problem1 {
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        int T = s.nextInt();
        while (T-- > 0) {
            int x = s.nextInt();
            System.out.println(1 + " " + (x - 1));
        }
    }
}
